[TrackScript("test_TRACK")]
public class testTRACK : TrackScript
{
	protected override void Load()
	{
		SetId("test_TRACK");
		//SetMap("f_gele_57_1");
		//CenterPos(-312.01,142.39,-359.01);
	}

	public override IActor[] OnStart(Character character, Track track)
	{
		base.OnStart(character, track);

		var actors = new List<IActor>();
		var mob0 = Shortcuts.AddMonster(0, 153006, "", "f_gele_57_1", -307.69, 147.65, -365.03, 0);
		mob0.SetVisibilty(ActorVisibility.Track, character.ObjectId);
		mob0.AddEffect(new ScriptInvisibleEffect());
		mob0.Layer = character.Layer;
		actors.Add(mob0);

		character.Movement.MoveTo(new Position(-312.0063f, 142.3877f, -359.0064f));
		actors.Add(character);

		var mob1 = Shortcuts.AddMonster(0, 147355, "", "f_gele_57_1", -344.9515, 168.8233, -513.7375, 0);
		mob1.SetVisibilty(ActorVisibility.Track, character.ObjectId);
		mob1.AddEffect(new ScriptInvisibleEffect());
		mob1.Layer = character.Layer;
		actors.Add(mob1);

		var mob2 = Shortcuts.AddMonster(0, 147355, "", "f_gele_57_1", -254.1299, 95.9891, 248.3621, 0);
		mob2.SetVisibilty(ActorVisibility.Track, character.ObjectId);
		mob2.AddEffect(new ScriptInvisibleEffect());
		mob2.Layer = character.Layer;
		actors.Add(mob2);

		return actors.ToArray();
	}

	public override async Task OnProgress(Character character, Track track, int frame)
	{
		switch (frame)
		{
			default:
				Log.Warning("OnProgress: Unsupported frame {0} called from {1}.", frame, this.TrackId);
				break;
		}
		await base.OnProgress(character, track, frame);
	}

}
